      CALL ZOOM_SETUP(
     i                LDAS%CAT_SFILE,VEGCLSLU,                
     o                NCATM,CAT%NFF,CAT%LATT,CAT%LONN)             
      IF (NCATM.NE.LDAS%NCATM) THEN
       WRITE(*,*) 'Number of catchments is',LDAS%NCATM
       WRITE(*,*) 'Number of catchments should be',NCATM
       STOP
      ENDIF

!=== Assign parameters to LDAS domain
      CALL ZVEG(
     i          LDAS%NCATM,CAT%NFF,GREENLU1,GREENLU2,GREENLU3,
     i          GREENLU4,GREENLU5,GREENLU6,GREENLU7,GREENLU8,
     i          GREENLU9,GREENLU10,GREENLU11,GREENLU12,LAILU1,
