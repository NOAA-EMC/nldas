      ALLOCATE (GREENLU9(LDAS%NCAT))
      ALLOCATE (GREENLU10(LDAS%NCAT))
      ALLOCATE (GREENLU11(LDAS%NCAT))
      ALLOCATE (GREENLU12(LDAS%NCAT))
      ALLOCATE (LAILU1(LDAS%NCAT))
      ALLOCATE (LAILU2(LDAS%NCAT))
      ALLOCATE (LAILU3(LDAS%NCAT))
      ALLOCATE (LAILU4(LDAS%NCAT))
      ALLOCATE (LAILU5(LDAS%NCAT))
      ALLOCATE (LAILU6(LDAS%NCAT))
      ALLOCATE (LAILU7(LDAS%NCAT))
      ALLOCATE (LAILU8(LDAS%NCAT))
      ALLOCATE (LAILU9(LDAS%NCAT))
      ALLOCATE (LAILU10(LDAS%NCAT))
      ALLOCATE (LAILU11(LDAS%NCAT))
      ALLOCATE (LAILU12(LDAS%NCAT))
      ALLOCATE (ZOLLU1(LDAS%NCAT))
      ALLOCATE (ZOLLU2(LDAS%NCAT))
      ALLOCATE (ZOLLU3(LDAS%NCAT))
      ALLOCATE (ZOLLU4(LDAS%NCAT))
      ALLOCATE (ZOLLU5(LDAS%NCAT))
      ALLOCATE (ZOLLU6(LDAS%NCAT))
      ALLOCATE (ZOLLU7(LDAS%NCAT))
      ALLOCATE (ZOLLU8(LDAS%NCAT))
      ALLOCATE (ZOLLU9(LDAS%NCAT))
      ALLOCATE (ZOLLU10(LDAS%NCAT))
      ALLOCATE (ZOLLU11(LDAS%NCAT))
      ALLOCATE (ZOLLU12(LDAS%NCAT))
      ALLOCATE (ALBEDOLU1(LDAS%NCAT))
      ALLOCATE (ALBEDOLU2(LDAS%NCAT))
      ALLOCATE (ALBEDOLU3(LDAS%NCAT))
      ALLOCATE (ALBEDOLU4(LDAS%NCAT))
      ALLOCATE (ALBEDOLU5(LDAS%NCAT))
      ALLOCATE (ALBEDOLU6(LDAS%NCAT))
      ALLOCATE (ALBEDOLU7(LDAS%NCAT))
      ALLOCATE (ALBEDOLU8(LDAS%NCAT))
      ALLOCATE (ALBEDOLU9(LDAS%NCAT))
      ALLOCATE (ALBEDOLU10(LDAS%NCAT))
      ALLOCATE (ALBEDOLU11(LDAS%NCAT))
      ALLOCATE (ALBEDOLU12(LDAS%NCAT))
      ALLOCATE (VGDLU1(LDAS%NCAT))
      ALLOCATE (VGDLU2(LDAS%NCAT))
      ALLOCATE (VGDLU3(LDAS%NCAT))
      ALLOCATE (VGDLU4(LDAS%NCAT))
      ALLOCATE (VGDLU5(LDAS%NCAT))
      ALLOCATE (VGDLU6(LDAS%NCAT))
      ALLOCATE (VGDLU7(LDAS%NCAT))
      ALLOCATE (VGDLU8(LDAS%NCAT))
      ALLOCATE (VGDLU9(LDAS%NCAT))
      ALLOCATE (VGDLU10(LDAS%NCAT))
