      DEALLOCATE (ALBEDOLU8)
      DEALLOCATE (ALBEDOLU9)
      DEALLOCATE (ALBEDOLU10)
      DEALLOCATE (ALBEDOLU11)
      DEALLOCATE (ALBEDOLU12)

      RETURN
      END

