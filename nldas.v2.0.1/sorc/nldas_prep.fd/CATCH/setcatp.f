      DEALLOCATE (LAILU9)
      DEALLOCATE (LAILU10)
      DEALLOCATE (LAILU11)
      DEALLOCATE (LAILU12)
      DEALLOCATE (ZOLLU1)
      DEALLOCATE (ZOLLU2)
      DEALLOCATE (ZOLLU3)
      DEALLOCATE (ZOLLU4)
      DEALLOCATE (ZOLLU5)
      DEALLOCATE (ZOLLU6)
      DEALLOCATE (ZOLLU7)
      DEALLOCATE (ZOLLU8)
      DEALLOCATE (ZOLLU9)
      DEALLOCATE (ZOLLU10)
      DEALLOCATE (ZOLLU11)
      DEALLOCATE (ZOLLU12)
      DEALLOCATE (VGDLU1)
      DEALLOCATE (VGDLU2)
      DEALLOCATE (VGDLU3)
      DEALLOCATE (VGDLU4)
      DEALLOCATE (VGDLU5)
      DEALLOCATE (VGDLU6)
      DEALLOCATE (VGDLU7)
      DEALLOCATE (VGDLU8)
      DEALLOCATE (VGDLU9)
      DEALLOCATE (VGDLU10)
      DEALLOCATE (VGDLU11)
      DEALLOCATE (VGDLU12)
      DEALLOCATE (ALBEDOLU1)
