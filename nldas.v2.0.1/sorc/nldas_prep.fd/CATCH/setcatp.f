       LAI5(N)=LAILU5(NFF(N))
       LAI6(N)=LAILU6(NFF(N))
       LAI7(N)=LAILU7(NFF(N))
       LAI8(N)=LAILU8(NFF(N))
       LAI9(N)=LAILU9(NFF(N))
       LAI10(N)=LAILU10(NFF(N))
       LAI11(N)=LAILU11(NFF(N))
       LAI12(N)=LAILU12(NFF(N))
       ZOL1(N)=ZOLLU1(NFF(N))
