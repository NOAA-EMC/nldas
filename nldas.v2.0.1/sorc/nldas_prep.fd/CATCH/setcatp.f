      REAL, pointer :: VGDLU1(:),VGDLU2(:),VGDLU3(:),VGDLU4(:),VGDLU5(:)
      REAL, pointer :: VGDLU6(:),VGDLU7(:),VGDLU8(:),VGDLU9(:)
      REAL, pointer :: VGDLU10(:),VGDLU11(:),VGDLU12(:) 
      CHARACTER*100 :: FILENAME

!=== End Variable Definition =============================================

!=== Allocate Space to Scratch Variables =================================
      FILENAME=LDAS%CAT_SFILE(1:index(LDAS%CAT_SFILE,' ')-1)//
     &         '/catchment.def'
      OPEN (10,FILE=FILENAME,FORM='FORMATTED',STATUS='OLD')
      READ (10,*) LDAS%NCAT
      CLOSE (10,STATUS='KEEP')
      ALLOCATE (VEGCLSLU(LDAS%NCAT))
      ALLOCATE (DPTHLU(LDAS%NCAT))
      ALLOCATE (POROSLU(LDAS%NCAT))
      ALLOCATE (BF1LU(LDAS%NCAT))
      ALLOCATE (BF2LU(LDAS%NCAT))
      ALLOCATE (BF3LU(LDAS%NCAT))
      ALLOCATE (ARS1LU(LDAS%NCAT))
      ALLOCATE (ARS2LU(LDAS%NCAT))
      ALLOCATE (ARS3LU(LDAS%NCAT))
      ALLOCATE (ARA1LU(LDAS%NCAT))
      ALLOCATE (ARA2LU(LDAS%NCAT))
      ALLOCATE (ARA3LU(LDAS%NCAT))
      ALLOCATE (ARA4LU(LDAS%NCAT))
