     o          CAT%GREEN(5),CAT%GREEN(6),CAT%GREEN(7),CAT%GREEN(8),
     o          CAT%GREEN(9),CAT%GREEN(10),CAT%GREEN(11),CAT%GREEN(12),
     o          CAT%LAI(1),CAT%LAI(2),CAT%LAI(3),CAT%LAI(4),CAT%LAI(5),
     o          CAT%LAI(6),CAT%LAI(7),CAT%LAI(8),CAT%LAI(9),CAT%LAI(10),
     o          CAT%LAI(11),CAT%LAI(12),CAT%ZOL(1),CAT%ZOL(2),
     o          CAT%ZOL(3),CAT%ZOL(4),CAT%ZOL(5),CAT%ZOL(6),CAT%ZOL(7),
     o          CAT%ZOL(8),CAT%ZOL(9),CAT%ZOL(10),CAT%ZOL(11),
     o          CAT%ZOL(12),CAT%ALBEDO(1),CAT%ALBEDO(2),CAT%ALBEDO(3),
     o          CAT%ALBEDO(4),CAT%ALBEDO(5),CAT%ALBEDO(6),CAT%ALBEDO(7),
     o          CAT%ALBEDO(8),CAT%ALBEDO(9),CAT%ALBEDO(10),
     o          CAT%ALBEDO(11),CAT%ALBEDO(12),CAT%VGD(1),CAT%VGD(2),
     o          CAT%VGD(3),CAT%VGD(4),CAT%VGD(5),CAT%VGD(6),CAT%VGD(7),
     o          CAT%VGD(8),CAT%VGD(9),CAT%VGD(10),CAT%VGD(11),
     o          CAT%VGD(12))

      CALL ZOOM(
     i          LDAS%NCATM,CAT%NFF,TSA1LU,TSA2LU,TSB1LU,TSB2LU,
     i          ARW1LU,ARW2LU,ARW3LU,ARW4LU,ARA1LU,ARA2LU, 
     i          ARA3LU,ARA4LU,ARS1LU,ARS2LU,ARS3LU,BF1LU, 
     i          BF2LU,BF3LU,POROSLU,DPTHLU,VEGCLSLU,       
     o          CAT%TSA(1),CAT%TSA(2),CAT%TSB(1),CAT%TSB(2),
     o          CAT%ARW(1),CAT%ARW(2),CAT%ARW(3),CAT%ARW(4), 
     o          CAT%ARA(1),CAT%ARA(2),CAT%ARA(3),CAT%ARA(4),  
     o          CAT%ARS(1),CAT%ARS(2),CAT%ARS(3),CAT%BF(1),    
     o          CAT%BF(2),CAT%BF(3),CAT%POROS,CAT%WPWET,CAT%BEE,
     o          CAT%ZDPTH(1),CAT%ZDPTH(2),CAT%ZDPTH(3),CAT%CDCR(1), 
     o          CAT%CDCR(2),CAT%PSIS,CAT%COND,CAT%GNU,CAT%VGWMAX,
     o          CAT%VEGCLS)

!=== De-allocate Space to Scratch Variables ==============================

      DEALLOCATE (VEGCLSLU)
      DEALLOCATE (DPTHLU)
      DEALLOCATE (POROSLU)
      DEALLOCATE (BF1LU)
      DEALLOCATE (BF2LU)
      DEALLOCATE (BF3LU)
      DEALLOCATE (ARS1LU)
      DEALLOCATE (ARS2LU)
      DEALLOCATE (ARS3LU)
      DEALLOCATE (ARA1LU)
      DEALLOCATE (ARA2LU)
      DEALLOCATE (ARA3LU)
      DEALLOCATE (ARA4LU)
      DEALLOCATE (ARW1LU)
      DEALLOCATE (ARW2LU)
      DEALLOCATE (ARW3LU)
      DEALLOCATE (ARW4LU)
      DEALLOCATE (TSA1LU)
      DEALLOCATE (TSA2LU)
      DEALLOCATE (TSB1LU)
      DEALLOCATE (TSB2LU)
      DEALLOCATE (GREENLU1)
      DEALLOCATE (GREENLU2)
      DEALLOCATE (GREENLU3)
      DEALLOCATE (GREENLU4)
      DEALLOCATE (GREENLU5)
      DEALLOCATE (GREENLU6)
      DEALLOCATE (GREENLU7)
      DEALLOCATE (GREENLU8)
      DEALLOCATE (GREENLU9)
      DEALLOCATE (GREENLU10)
      DEALLOCATE (GREENLU11)
      DEALLOCATE (GREENLU12)
      DEALLOCATE (LAILU1)
      DEALLOCATE (LAILU2)
      DEALLOCATE (LAILU3)
      DEALLOCATE (LAILU4)
      DEALLOCATE (LAILU5)
      DEALLOCATE (LAILU6)
      DEALLOCATE (LAILU7)
